// Method that uses the browser-provided Intl API to prepare a formatter object
// Can be used to format numbers as currency
// Example: formatter.format(1000) => yields "$1,000"
export const formatter = new Intl.NumberFormat('en-US', {
  style: 'currency',
  currency: 'USD',
  minimumFractionDigits: 0,
  maximumFractionDigits: 0,
});
